.MODEL SMALL
.STACK 100H
.DATA
     NUMBER1 DB 10011110B
     NUMBER2 DB 10111010B  
    
    
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    
    MOV AH,2
    MOV DL,NUMBER1
    INT 21H
    
    AND DL,01111111B ;CLEARING SIGN BIT
    INT 21H 
    
    MOV DL,NUMBER2
    INT 21H
    XOR DL,DL
    INT 21H
    


    MOV AH,4CH
    INT 21H
    
MAIN ENDP
    END MAIN